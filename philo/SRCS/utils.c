#include "../HDRS/philo.h"

ssize_t	_atoi(char *str)
{
	int		i;
	ssize_t	res;

	i = 0;
	res = 0;
	if (str[i] == '+')
		++i;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res *= 10 + (str[i] - 48);
		++i;
	}
	if (str[i] != 0)
		return (-1);
	return (res);
}
